function normalize(value) {
  return String(value || "")
    .trim()
    .toLowerCase();
}

function hashString(value) {
  return normalize(value)
    .split("")
    .reduce((sum, character) => sum + character.charCodeAt(0), 0);
}

export function getCropMeta(name) {
  const normalized = normalize(name);
  return (
    CROP_OPTIONS.find(
      (crop) => crop.id === normalized || normalize(crop.name) === normalized
    ) || CROP_OPTIONS[0]
  );
}

export function getLocationProfile(location) {
  const key = String(location || "").trim();
  const profile = LOCATION_META[key] || LOCATION_META.Nipani;

  return {
    ...profile,
    village: key || profile.village,
    coordinates: profile.coordinates || [16.40, 74.38],
    fullAddress: `${key || profile.village}, ${profile.taluk}, ${profile.district}, ${profile.state}, India`,
  };
}

export function getFarmerProfile(name, location) {
  const normalizedName = normalize(name);
  const directMatch =
    FARMER_PROFILES.find(
      (profile) =>
        normalize(profile.name) === normalizedName ||
        normalizedName.includes(normalize(profile.name).split(" ")[0])
    ) || FARMER_PROFILES[hashString(name) % FARMER_PROFILES.length];

  const ratingSeed = hashString(`${name}-${location}`);
  const rating = Number((4.5 + ((ratingSeed % 5) * 0.1)).toFixed(1));

  return {
    ...directMatch,
    name: String(name || directMatch.name).trim() || directMatch.name,
    village: String(location || directMatch.village).trim() || directMatch.village,
    rating,
  };
}

export function getCategoryMeta(category) {
  return CATEGORY_META[category] || CATEGORY_META.Vegetables;
}

export function getListingDetailMeta(listing) {
  const crop = getCropMeta(listing?.crop);
  const location = getLocationProfile(listing?.location);
  const createdAt = listing?.createdAt ? new Date(listing.createdAt) : new Date();
  const hashSeed = hashString(
    `${listing?._id || ""}-${listing?.farmer || ""}-${listing?.location || ""}`
  );
  const landAreaAcres = Number(
    Math.max(1.5, (Number(listing?.quantity || 0) / 85) + ((hashSeed % 4) * 0.4)).toFixed(1)
  );
  const listingWindowDays = 30 + (hashSeed % 21);
  const closeDate = new Date(createdAt);
  closeDate.setDate(closeDate.getDate() + listingWindowDays);
  const daysLeft = Math.max(
    1,
    Math.ceil((closeDate.getTime() - Date.now()) / (1000 * 60 * 60 * 24))
  );
  const farmerCode = 8000 + (hashSeed % 900);

  return {
    crop,
    location,
    createdAt,
    closeDate,
    daysLeft,
    landAreaAcres,
    farmerCode,
    cropCode: `${farmerCode}/GM`,
    localCropLine: `${crop.name} | ${crop.localNames.kannada} | ${crop.localNames.marathi}`,
  };
}
